     */
    public function store(Request $request)
    {
        $post =new donate();
        $post->type=$request->type; 
    
        $post->phone=$request->phone; 
        $post->email=$request->email; 
        
        $post->amount=$request->amount; 
     
        if($post->save()) 
        {
            return new donateresource($post);
        }
    }

